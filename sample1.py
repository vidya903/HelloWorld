# Counting Number of vowels
name=input("Enter a Name")
count=0
for char in name:
  if char=="a":
    count=count+1
  elif char =="e":
    count=count+1
  elif char =="i":
    count=count+1
  elif char=="o":
    count=count+1
  elif char =="u":
    count=count+1
print("Total Number of vowels are ",count)



